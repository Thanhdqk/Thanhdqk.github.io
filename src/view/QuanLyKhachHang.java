    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(txtMaKH.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Mã khách hàng trống !");
            return;
        }
        if(txtHoTen.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Tên khách hàng trống !");
            return;
        }
        if(txtSoDienThoai.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Số điện thoại trống !");
            return;
        }
        if(!isValidPhoneNumber(txtSoDienThoai.getText())){
            JOptionPane.showMessageDialog(this, "Số điện thoại không đúng định dạng !");
            return;
        }
        if(!isValidEmail(txtEmail.getText())){
            JOptionPane.showMessageDialog(this, "Email không đúng định dạng !");
            return;
        }
        if(txtEmail.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Email trống !");
            return;
        }
        if(txtDiaChi.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Địa chỉ trống !");
            return;
        }else{
            insert();
        }

    }//GEN-LAST:event_btnAddActionPerformed

